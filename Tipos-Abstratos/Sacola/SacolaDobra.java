public class SacolaDobra {
    public static void Dobra(Sacola dobra) {
        for (int i = 0; i <= dobra.getTamanho(); i++) {
            if (dobra.tamanho() == dobra.getTamanho()) {
                int multi = dobra.getTamanho() * 2;
                dobra.setTamanho(multi);
            }
        }
    }
}